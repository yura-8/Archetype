namespace SimpleRpg
{
    /// <summary>
    /// 戦闘時の計算に使用するパラメータをまとめたクラスです。
    /// </summary>
    public class BattleParameter
    {
        /// <summary>
        /// 計算に使用する攻撃力です。
        /// </summary>
        public int atk;

        /// <summary>
        /// 計算に使用する防御力です。
        /// </summary>
        public int def;

        /// <summary>
        /// 計算に使用する素早さです。
        /// </summary>
        public int dex;
    }
}